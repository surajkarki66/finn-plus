class LogLevel(str, Enum):
    """Log levels printed on the commandline for the build process."""

    NONE = "NONE"
    DEBUG = "DEBUG"
    INFO = "INFO"
    WARNING = "WARNING"
    ERROR = "ERROR"
    CRITICAL = "CRITICAL"


def to_logging_level(level: LogLevel) -> int:
    """Return the logging module loglevel for the StrEnum LogLevel value."""
    match level:
        case LogLevel.NONE:
            return 0
        case LogLevel.DEBUG:
            return logging.DEBUG
        case LogLevel.INFO:
            return logging.INFO
        case LogLevel.WARNING:
            return logging.WARNING
        case LogLevel.ERROR:
            return logging.ERROR
        case LogLevel.CRITICAL:
            return logging.CRITICAL
        case _:
            return 0


